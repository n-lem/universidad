
# Estructuras iterativas while
# 15. Escribir un programa que muestre los números del 0 al 50, pero de 5 en 5.


# Indico el número desde el cual voy a comenzar la cuenta regresiva
inicio = 50

# Recorro desde el valor inicial hasta el 0 inclusive
while inicio >= 0:
    # Imprimo el número obtenido
    print(inicio)
    # Por cada ciclo, se restan cinco unidades
    inicio = inicio - 5

