
# 5. Escribir un programa que calcule el área de un triángulo, sabiendo que :
# ● La base (b) mide 20.5
# ● La altura (h) es igual a la base al cuadrado
# Investigue cual es la fórmula para calcular el área de un triángulo y 
# utilicela en el programa luego muestre por pantalla el resultado.


# Indico la base del triángulo
base = 20.5

# Calculo la altura del triángulo
altura = base ** 2

# Calculo el área del triángulo con su fórmula: (base * altura) / 2
area = (base * altura)/2

# Muestro el resultado por pantalla
print('El área del triángulo es de:',area)

