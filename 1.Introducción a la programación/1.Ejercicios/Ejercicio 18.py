# Escribir un programa que calcule el área de un triángulo, sabiendo que:
# - La base (b) mide 20.5.
# - La altura (h) es igual a la base al cuadrado
# Investigue cual es la fórmula para calcular el área de un triángulo y utilícela en el
# programa luego muestre por pantalla el resultado.

#Datos del triángulo
base = 20.5
altura = base ** 2

# Area del triángulo
area = (base * altura) / 2

print(f"El área del triángulo es: {area}")